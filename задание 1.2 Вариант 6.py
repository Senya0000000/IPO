# Создайте пустой массив для хранения введенных чисел
arr = []

# Вводите значения с клавиатуры и добавляйте их в массив
for i in range(10):
    num = int(input(f"Введите целое число {i + 1}: "))
    arr.append(num)

# Инициализируйте переменную для суммы чисел больших 5
sum_greater_than_5 = 0

# Пройдитесь по массиву и найдите сумму чисел больших 5
for num in arr:
    if num > 5:
        sum_greater_than_5 += num

# Выведите результат
print(f"Сумма чисел, больших 5: {sum_greater_than_5}")
